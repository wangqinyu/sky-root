package com.sky.skyadapterkidcrm.bean;

public class KidcrmBodyResp {
    private String registrationAmount;
    private String effectiveAmount;
    private String visitAmount;
    private String auditionAmount;
    private String paymentAmount;
    private String paymentMoney;

    public String getRegistrationAmount() {
        return registrationAmount;
    }

    public void setRegistrationAmount(String registrationAmount) {
        this.registrationAmount = registrationAmount;
    }

    public String getEffectiveAmount() {
        return effectiveAmount;
    }

    public void setEffectiveAmount(String effectiveAmount) {
        this.effectiveAmount = effectiveAmount;
    }

    public String getVisitAmount() {
        return visitAmount;
    }

    public void setVisitAmount(String visitAmount) {
        this.visitAmount = visitAmount;
    }

    public String getAuditionAmount() {
        return auditionAmount;
    }

    public void setAuditionAmount(String auditionAmount) {
        this.auditionAmount = auditionAmount;
    }

    public String getPaymentAmount() {
        return paymentAmount;
    }

    public void setPaymentAmount(String paymentAmount) {
        this.paymentAmount = paymentAmount;
    }

    public String getPaymentMoney() {
        return paymentMoney;
    }

    public void setPaymentMoney(String paymentMoney) {
        this.paymentMoney = paymentMoney;
    }
}
